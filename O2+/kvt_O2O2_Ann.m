krate(i)=0.25*(abcd(i_here,1)*T^3+abcd(i_here,2)*T^2 ...
    +abcd(i_here,3)*T+abcd(i_here,4));

krate=krate/1e6;
kbf=krate.*exp(-(O2.e_i(1,2:end)-O2.e_i(1,1:end-1))/k/T);
