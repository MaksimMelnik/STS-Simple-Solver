i=1:6;
krate(i)=0.25*exp((-2.16870E-03*i.^3+1.02950E-02*i.^2-9.03306E-02*i...
    +6.02557)*log(T)+(2.78130E-02*i.^3-2.76608E-01*i.^2+2.04169*i...
    -76.0376));
i=7:32;
krate(i)=0.25*(2.44106E-33*T^6.25015)*exp((1.76911E-07*T^2 ...
    -4.82968E-04*T+5.40946E-01).*i);
i=33:41;
abcd=[  8.11526E-20 -1.62934E-16 1.34668E-13 -2.19694E-11 % 33
        6.59091E-20 -1.40487E-16 1.27056E-13 -1.76905E-11
        7.60101E-20 -1.66732E-16 1.43746E-13 -1.61310E-11
        7.57187E-20 -1.61678E-16 1.32137E-13 -7.15596E-12
        3.60748E-20 -7.34407E-17 6.74910E-14 1.04153E-11
        3.40294E-20 -6.43874E-17 5.61750E-14 1.57281E-11
        1.76281E-20 -3.04842E-17 3.28204E-14 2.21318E-11
        1.11133E-20 -1.56706E-17 2.12930E-14 2.56952E-11
        1.24648E-20 -1.79119E-17 2.19332E-14 2.61423E-11];% 41
